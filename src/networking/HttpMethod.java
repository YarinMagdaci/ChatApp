package networking;

/**
 * Represents the HTTP methods used in HTTP-based communication.
 * The enum defines the common HTTP methods: GET, POST, PUT, and DELETE.
 * These methods are used to specify the desired action to be performed on a resource when making HTTP requests.
 */
public enum HttpMethod {
    GET, POST, PUT, DELETE
}
